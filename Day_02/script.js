// var, let and const

// var
var message1; // Declare the variable 
message1 = "Hello this is massage"; // String initialization
console.log(message1); 

var message2;       // Declare a variable 
message2 = "Hello";         // First initialization
message2 = "Hello, JavaScript";      // Second initialization
console.log(message2);


// let 
let greeting = "Hello Js"; // String initialization
console.log(greeting);


// const
const pi = 3.1416;      // Declare the values to pi
console.log(pi);

